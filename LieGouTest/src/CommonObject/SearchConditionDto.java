package CommonObject;

public class SearchConditionDto {
	private String name;
	
	private String intentionalProfession;//意向职位
	private String desiredPlace;//期望工作地
	private String updateTime;//更新时间
	private String experience;//工作经验
	private String educationustom;//学历
	private String age;//年龄
	private String sex;//性别
	
	public String getIntentionalProfession() {
		return intentionalProfession;
	}
	public void setIntentionalProfession(String intentionalProfession) {
		this.intentionalProfession = intentionalProfession;
	}
	public String getDesiredPlace() {
		return desiredPlace;
	}
	public void setDesiredPlace(String desiredPlace) {
		this.desiredPlace = desiredPlace;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getExperience() {
		return experience;
	}
	public void setExperience(String experience) {
		this.experience = experience;
	}
	public String getEducationustom() {
		return educationustom;
	}
	public void setEducationustom(String educationustom) {
		this.educationustom = educationustom;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
